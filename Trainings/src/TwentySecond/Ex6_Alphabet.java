package TwentySecond;

public class Ex6_Alphabet {
    public static void main(String[] args) {
        for ( char i = 'a'; i <= 'z'; i++) {
            System.out.println(i);
        }

        for ( char k = 'z'; k >= 'a'; k--) {
            System.out.println(k);
        }

        for ( char i = 'A'; i <= 'Z'; i++) {
            System.out.println(i);
        }

    }
}
